package lab5;

import java.util.Scanner;

public class lab5_2 {


    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        final double PI = 3.141;

        System.out.print("Enter r: ");
        double r = sc.nextDouble();

        double c = 2 * PI * r;
        System.out.println("Circle round: " + c);





    }
}
